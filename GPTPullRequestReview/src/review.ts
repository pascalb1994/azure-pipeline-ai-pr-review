import fetch from 'node-fetch';
import { git } from './git';
import { OpenAIApi } from 'openai';
import { addCommentToPR } from './pr';
import { Agent } from 'https';
import { HttpsProxyAgent } from 'https-proxy-agent';
import * as tl from "azure-pipelines-task-lib/task";

export async function reviewFile(targetBranch: string, fileName: string, httpsAgent: Agent, apiKey: string, openai: OpenAIApi | undefined, aoiEndpoint: string | undefined, proxyUrl: string | undefined) {
  console.log(`\nStart reviewing ${fileName} ...`);
  
  const defaultOpenAIModel = 'gpt-3.5-turbo';
  const patch = await git.diff([targetBranch, '--', fileName]);

  const instructions = tl.getInput('ai_instructions')

  try {
    let choices: any;

    if (openai) {

      console.log(`Sending changes to OpenAI:`)
      console.log(patch)

      const response = await openai.createChatCompletion({
        model: tl.getInput('model') || defaultOpenAIModel,
        messages: [
          {
            role: "system",
            content: instructions
          },
          {
            role: "user",
            content: patch
          }
        ],
        max_tokens: 500
      });

      choices = response.data.choices
    }
    else if (aoiEndpoint) {
      if(proxyUrl && proxyUrl.length > 0) {
         const proxyAgent = new HttpsProxyAgent(proxyUrl);
         const request = await fetch(aoiEndpoint, {
            method: 'POST',
            headers: { 'api-key': `${apiKey}`, 'Content-Type': 'application/json' },
            body: JSON.stringify({
              max_tokens: 500,
              messages: [{
                role: "user",
                content: prompt
              }]
            }),
            proxyAgent
          });
    
          const response = await request.json();
          choices = response.choices;
      } else {      
        const request = await fetch(aoiEndpoint, {
          method: 'POST',
          headers: { 'api-key': `${apiKey}`, 'Content-Type': 'application/json' },
          body: JSON.stringify({
            max_tokens: 500,
            messages: [{
              role: "user",
              content: prompt
            }]
          })
        });
  
        const response = await request.json();  
        choices = response.choices;
      }     
    }

    if (choices && choices.length > 0) {
      const review = choices[0].message?.content as string;

      if (review.trim() !== "No feedback.") {
        await addCommentToPR(fileName, review, httpsAgent);
      }
    }

    console.log(`Review of ${fileName} completed.`);
  }
  catch (error: any) {
    if (error.response) {
      console.log(error.response.status);
      console.log(error.response.data);
    } else {
      console.log(error.message);
    }
  }
}
